
public class CompareTutorial {
    public static void main(String[] args) {
        // result = true || false
        int a = 10, b = 5;
        // boolean c = (a == b);
        System.out.println("(a == b) => " + (a == b));
        System.out.println("(a != b) => " + (a != b));
        System.out.println("(a > b) => " + (a > b));
        System.out.println("(a >= b) => " + (a >= b));
        System.out.println("(a < b) => " + (a < b));
        System.out.println("(a <= b) => " + (a <= b));
    }
}
