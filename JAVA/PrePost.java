
public class PrePost {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        int c = 5;
        int d = 5;

        System.out.println("++a : " + ++a); // บวกก่อนใช้
        System.out.println("b++ : " + b++); // ใช้ก่อนบวก
        System.out.println("--c : " + --c); // ลบก่อนใช้
        System.out.println("d-- : " + d--); // ใช้ก่อนลบ
    }
}
